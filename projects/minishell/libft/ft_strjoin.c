#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	char	*str;
	size_t	size1;
	size_t	size2;
	size_t	i;

	i = 0;
	size1 = ft_strlen(s1);
	size2 = ft_strlen(s2);
	str = malloc(size1 + size2 + 1);
	if (!str)
		return (0);
	while (*s1)
	{
		str[i] = *s1;
		s1++;
		i++;
	}
	while (*s2)
	{
		str[i] = *s2;
		s2++;
		i++;
	}
	str[i] = '\0';
	return (str);
}
