#include "libft.h"

int	ft_putstr(const char *s)
{
	int	i;

	i = -1;
	if (s == NULL)
		return (ft_putstr("(null)"));
	while (s[++i] != 0)
		write(1, &s[i], 1);
	return (i);
}
